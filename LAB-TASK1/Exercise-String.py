
# lets explore and use some of the string methods listed in dir(s) for the string s = 'abc',

s='abc' #string

print(s.capitalize()) #Output Abc
print(s.upper()) #Output ABC
print(s.lower()) #Output abc
print(s.find('b')) #Output 1
print(s.replace('a','b')) #Output bbc
print(s.startswith('a')) #Output True
print(s.split('b'))  # Output: ['a', 'c']

